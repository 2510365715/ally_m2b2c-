package com.ally.search.po;

public class Item {
	private String id;
	
	private String pro_name;
	
	private String cate_name;
	
	private float price;
	
	private String picture;
	
	private String sell_point;
	
	private String details;
	
	private int f_Categoryid;
	
	private int s_Categoryid;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the pro_name
	 */
	public String getPro_name() {
		return pro_name;
	}

	/**
	 * @param pro_name the pro_name to set
	 */
	public void setPro_name(String pro_name) {
		this.pro_name = pro_name;
	}

	/**
	 * @return the cate_name
	 */
	public String getCate_name() {
		return cate_name;
	}

	/**
	 * @param cate_name the cate_name to set
	 */
	public void setCate_name(String cate_name) {
		this.cate_name = cate_name;
	}

	/**
	 * @return the price
	 */
	public float getPrice() {
		return price;
	}

	/**
	 * @param price the price to set
	 */
	public void setPrice(float price) {
		this.price = price;
	}

	/**
	 * @return the picture
	 */
	public String getPicture() {
		return picture;
	}

	/**
	 * @param picture the picture to set
	 */
	public void setPicture(String picture) {
		this.picture = picture;
	}

	/**
	 * @return the sell_point
	 */
	public String getSell_point() {
		return sell_point;
	}

	/**
	 * @param sell_point the sell_point to set
	 */
	public void setSell_point(String sell_point) {
		this.sell_point = sell_point;
	}

	/**
	 * @return the details
	 */
	public String getDetails() {
		return details;
	}

	/**
	 * @param details the details to set
	 */
	public void setDetails(String details) {
		this.details = details;
	}

	/**
	 * @return the f_Categoryid
	 */
	public int getF_Categoryid() {
		return f_Categoryid;
	}

	/**
	 * @param f_Categoryid the f_Categoryid to set
	 */
	public void setF_Categoryid(int f_Categoryid) {
		this.f_Categoryid = f_Categoryid;
	}

	/**
	 * @return the s_Categoryid
	 */
	public int getS_Categoryid() {
		return s_Categoryid;
	}

	/**
	 * @param s_Categoryid the s_Categoryid to set
	 */
	public void setS_Categoryid(int s_Categoryid) {
		this.s_Categoryid = s_Categoryid;
	}
	
	
}
